        static int GetTypeIndex(string type)
        {
            return Array.IndexOf(TYPES, type);
        }

        static float GetEffectiveness(string attackType, string[] defenseTypes)
        {
            float product = 1;

            for (int i = 0; i < defenseTypes.Length; i++)
            {
                product *= MULTIPLIERS[GetTypeIndex(attackType), GetTypeIndex(defenseTypes[i])];
            }

            return product;
        }

        static void SimulateAttack(string atkType, string[] defTypes)
        {
            string defTypesString = String.Join(" ", defTypes);
            Console.WriteLine("{0} -> {1} = {2}x", atkType, defTypesString, GetEffectiveness(atkType, defTypes));
        }


